package com.example.gmit_tiberias_kupang.Activity;

public class Jadwal_item {

    private  String id_jadwal;
    private  String id_kategori;
    private  String nama_ibadah;
    private  String jenis_ibadah;
    private  String tanggal;
    private  String jam_mulai;
    private  String jam_selesai;

    public Jadwal_item(){

    }

    public String getId_jadwal() {
        return id_jadwal;
    }

    public void setId_jadwal(String id_jadwal) {
        this.id_jadwal = id_jadwal;
    }

    public String getId_kategori() {
        return id_kategori;
    }

    public void setId_kategori(String id_kategori) {
        this.id_kategori = id_kategori;
    }

    public String getNama_ibadah() {
        return nama_ibadah;
    }

    public void setNama_ibadah(String nama_ibadah) {
        this.nama_ibadah = nama_ibadah;
    }

    public String getJenis_ibadah() {
        return jenis_ibadah;
    }

    public void setJenis_ibadah(String jenis_ibadah) {
        this.jenis_ibadah = jenis_ibadah;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getJam_mulai() {
        return jam_mulai;
    }

    public void setJam_mulai(String jam_mulai) {
        this.jam_mulai = jam_mulai;
    }

    public String getJam_selesai() {
        return jam_selesai;
    }

    public void setJam_selesai(String jam_selesai) {
        this.jam_selesai = jam_selesai;
    }






}
